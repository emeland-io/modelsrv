type Field struct {
	Name           string
	Type           string
	IsRef          bool
	RefType        string // for reference types
	MapKey         string // for looking up in model maps
	HasAnnotations bool
	// SkipAccessor keeps the struct field but omits generated Get/Set and interface entries
	// (use with CustomMethods + hand-written or companion implementations).
	SkipAccessor bool
}

// ParentLinkSpec generates GetParent / SetParent / SetParentByRef (+ optional GetParentId / SetParentById)
// for a skipped-accessor field like Parent *ContextRef | *SystemRef.
type ParentLinkSpec struct {
	FieldName         string // struct field, e.g. "Parent"
	RefTypeName       string // e.g. "ContextRef"
	ResourceTypeName  string // e.g. "Context"
	ResolvedMethod    string // e.g. "ResolvedContext"
	EffectiveIDMethod string // e.g. "EffectiveParentContextID"; empty skips GetParentId
	EmbedFieldName    string // literal field in ref struct, e.g. "Context"
	RefIDFieldName    string // e.g. "ContextId"
	ResourceIDGetter  string // e.g. "GetContextId"
	ModelLookupByID   string // godoc only, e.g. "GetContextById"
	// SetParentById(parentId) — id-only ref (e.g. Context); omit for System.
	SetParentByID bool
}

// TypeRefLinkSpec generates GetNodeType / SetTypeRef / SetNodeTypeByRef for TypeRef *NodeTypeRef.
type TypeRefLinkSpec struct {
	FieldName        string // e.g. "TypeRef"
	RefTypeName      string // e.g. "NodeTypeRef"
	ResourceTypeName string // e.g. "NodeType"
	ResolvedMethod   string // e.g. "ResolvedNodeType"
	EmbedFieldName   string // e.g. "NodeType"
	RefIDFieldName   string // e.g. "NodeTypeId"
	ResourceIDGetter string // e.g. "GetNodeTypeId"
	ModelLookupByID  string // godoc, e.g. "GetNodeTypeById"
}

// RefByRefSpec generates SetFooByRef(resource R) { o.SetFoo(&Ref{...}) } with optional nil guard.
type RefByRefSpec struct {
	MethodName       string // e.g. "SetApiRefByRef"
	ParamName        string // e.g. "api"
	ResourceTypeName string // e.g. "API"
	SetterName       string // e.g. "SetApiRef" — existing setter for *Ref
	RefTypeName      string // e.g. "ApiRef"
	EmbedFieldName   string // e.g. "API"
	RefIDFieldName   string // e.g. "ApiID"
	ResourceIDGetter string // e.g. "GetApiId"
	NilCheck         bool   // if true, emit if param==nil { o.Setter(nil); return }
}

type TypeSpec struct {
	Name          string
	EventType     string
	IDField       string
	NameField     string // field used for GetResourceName(); defaults to "DisplayName"
	Fields        []Field
	CustomMethods []string // additional method signatures to include in the interface
	// HasModel uses NewT(m Model, id) and stores model + sink (sink from m.GetSink()) for registry lookups.
	HasModel    bool
	ParentLink  *ParentLinkSpec  // optional parent *XxxRef helpers
	TypeRefLink *TypeRefLinkSpec // optional Node-style type ref helpers
	RefByRefs   []RefByRefSpec   // optional SetXByRef(resource) from embedded ref structs
}

